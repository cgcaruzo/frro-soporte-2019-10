def vocal(l):
    if l == 'a' or l ==  'A' or l == 'e' or l == 'E'  or l ==  'i'  or l ==  'I'  or l ==  'o'  or l ==  'O'  or l ==  'u'  or l ==  'U' :
        return True
    else :
        return False


assert(vocal('a') == True)
assert(vocal('A') == True)
assert(vocal('e') == True)
assert(vocal('E') == True)
assert(vocal('i') == True)
assert(vocal('I') == True)
assert(vocal('o') == True)
assert(vocal('O') == True)
assert(vocal('u') == True)
assert(vocal('U') == True)

